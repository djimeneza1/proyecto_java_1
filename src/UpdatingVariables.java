public class UpdatingVariables {
    public static void main(String[] args) {
        int salary = 1000;

        // bono
        salary = salary +200;
        System.out.println(salary);

        //pension: 50
        salary=salary-50;
        System.out.println(salary);

        String employeeName = "Daniel Jimenez";
        employeeName = employeeName + " Avalo";
        System.out.println("tu nombre es: "+employeeName);

    }
}
